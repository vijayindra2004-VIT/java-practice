    public void loadEmployeeNames() {

     try {

        comboName.removeActionListener(comboName.getActionListeners()[0]);

        Connection con = DBConnection.getConnection();

        String sql = "SELECT name FROM employees";

        PreparedStatement ps = con.prepareStatement(sql);
        ResultSet rs = ps.executeQuery();

        comboName.removeAllItems();

        comboName.addItem("Select Employee");

        while (rs.next()) {
            comboName.addItem(rs.getString("name"));
        }

        comboName.addActionListener(this::comboNameActionPerformed);

    } catch (Exception e) {
        e.printStackTrace();
    }

}

