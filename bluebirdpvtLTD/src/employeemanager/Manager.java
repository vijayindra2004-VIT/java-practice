    private void comboNameActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_comboNameActionPerformed
 if(comboName.getSelectedIndex() <= 0){
        return;
    }

    String name = comboName.getSelectedItem().toString();

    try {

        Connection con = DBConnection.getConnection();

        String sql = "SELECT * FROM employees WHERE name=?";

        PreparedStatement ps = con.prepareStatement(sql);
        ps.setString(1, name);

        ResultSet rs = ps.executeQuery();

        if(rs.next()){

            txtID.setText(rs.getString("id"));
            txtname.setText(rs.getString("Name"));
            txtdept.setText(rs.getString("department"));
            txtdesig.setText(rs.getString("designation"));
            txtsalary.setText(rs.getString("salary"));
            txtemail.setText(rs.getString("email"));
            txtphno.setText(rs.getString("phone"));
            txtdate.setText(rs.getString("joining_date"));
            txtlocation.setText(rs.getString("location"));

        }

    } catch(Exception e){
        e.printStackTrace();
    }   // TODO add your handling code here:
    }//GEN-LAST:event_comboNameActionPerformed

    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
 try{

        Connection con = DBConnection.getConnection();

        String sql="INSERT INTO employees(name,department,designation,salary,email,phone,joining_date,location) VALUES (?,?,?,?,?,?,?,?)";

        PreparedStatement ps=con.prepareStatement(sql);

        ps.setString(1, txtname.getText());
        ps.setString(2, txtdept.getText());
        ps.setString(3, txtdesig.getText());
        ps.setDouble(4, Double.parseDouble(txtsalary.getText()));
        ps.setString(5, txtemail.getText());
        ps.setString(6, txtphno.getText());
        ps.setString(7, txtdate.getText());
        ps.setString(8, txtlocation.getText());

        ps.executeUpdate();

        JOptionPane.showMessageDialog(this,"Employee Added Successfully");

        loadEmployeeNames();

    }catch(Exception e){
        e.printStackTrace();
    }        // TODO add your handling code here:
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
try{

        Connection con = DBConnection.getConnection();

        String sql="UPDATE employees SET name=?,department=?,designation=?,salary=?,email=?,phone=?,joining_date=?,location=? WHERE id=?";

        PreparedStatement ps=con.prepareStatement(sql);
        
        ps.setString(1, txtname.getText());
        ps.setString(2, txtdept.getText());
        ps.setString(3, txtdesig.getText());
        ps.setDouble(4, Double.parseDouble(txtsalary.getText()));
        ps.setString(5, txtemail.getText());
        ps.setString(6, txtphno.getText());
        ps.setString(7, txtdate.getText());
        ps.setString(8, txtlocation.getText());
        ps.setInt(9, Integer.parseInt(txtID.getText()));

        ps.executeUpdate();

        JOptionPane.showMessageDialog(this,"Employee Updated Successfully");

        loadEmployeeNames();

    }catch(Exception e){
        e.printStackTrace();
    }        // TODO add your handling code here:
    }//GEN-LAST:event_btnUpdateActionPerformed

